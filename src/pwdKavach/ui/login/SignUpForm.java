    // SignUp using Insert Operations  
    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        makeStarsInvisible();
        
        if(markEmptyFields()){
            JOptionPane.showMessageDialog(null, "Please enter all the fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!matchingPasswords()){
            JOptionPane.showMessageDialog(null, "Passwords not matching", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(handler.insertUser(txtUsername.getText(), String.valueOf(txtPassword1.getPassword()))){
             JOptionPane.showMessageDialog(null, "You Signed up Successfully", "Sucess", JOptionPane.INFORMATION_MESSAGE);
             showLoginForm();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Username already exist", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

